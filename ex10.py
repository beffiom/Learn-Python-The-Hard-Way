# initializes a string variable 'tabby_cat' that begins with a tab
tabby_cat = "\tI'm tabbed in."
# initializes a string variable 'persian_cat' that splits half the statement to a new line
persian_cat = "I'm split\non a line."
# initializes a string variable "backslash_cat" with backslashes between words
backslash_cat = "I'm \\ a \\ cat."

# initializes a string variable 'fat_cat' with each statement on a new line and some tabbed in.
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
